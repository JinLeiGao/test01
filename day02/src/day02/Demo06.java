package day02;

public class Demo06 {
	/*
	 * 变量的定义
	 *  A:数据类型 变量名 = 初始化值;
		B:数据类型 变量名;
		     变量名 = 变量值;
	 */
	public static void main(String[] args) {
		
		// 第一种   数据类型  标识符  = 初始化值     ----> 定义的时候赋值
		String person  = "赵丽颖";
		System.out.println(person);
		
		// 第二种     先定义  【先声明】 再赋值
		int x ; // 定义 
		x = 28; // 赋值    “=”
		System.out.println(x);
		
		
	}

}
