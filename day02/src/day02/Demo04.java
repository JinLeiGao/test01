package day02;

public class Demo04 {

	/**
	 * 字面值常量
	 * 
	 * 	 A:字符串常量 "hello" 
	 *   B:整数常量 12,23 
	 *   C:小数常量 12.345
	 *   D:字符常量 'a','A','0'
	 *   E:布尔常量 true,false
	 * 
	 */
	public static void main(String[] args) {

		final String a = "hello";
		
		// 整数
		final int b = 23;
		// 浮点数
		final double  c  =  0.618;
		
		//字符型
		final char  d  = 'A';  // 单引号
//		final  char e  =  "a";
		
		//布尔  boolean 只有两个值  (ture false)   
		final boolean f = true;
		final boolean h = false;
		

	}
}
